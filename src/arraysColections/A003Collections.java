package arraysColections;

public class A003Collections {

    //Colections são estruturas de dados

    /*Set (Conjunto):
    * Não ordenado
    * Não indexado
    * Não aceita repetição*/

    /*List (Lista):
    * indexada
    * aceita repetição*/

    /*Map (tabela):
    * Estrutura Chave/ Valor
    * Chave nao aceita repetição
    * valor aceita repetição*/

    /*Queue (Fila):
    * implementa uma fila
    * primeiro a entrar/ primeiro a sair (dados) (FIFO)*/

    /*Stack (pilha)
    * implementa uma pilha
    * ultimo a entrar/ primeiro a sair (dados) (LIFO)*/
}
