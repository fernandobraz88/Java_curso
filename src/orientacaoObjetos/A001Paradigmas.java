package orientacaoObjetos;

public class A001Paradigmas {

    //Toda Classe é uma estrutura de dados que instancia objetos como dados;
    //As variaveis/ atributos são as caracteristicas que representam o estado na OO;
    //Os métodos representam o comportamento do objeto quando manipula-se os atributos;
    //O acesso aos elementos e comportamentos de um objeto se faz via notaçao ponto;

    //Paradigmas da Orientação a Objetos:

    //Abstração:
    /*É a simplificação de objetos complexos em representações mais simples, focando apenas nos aspectos essenciais para
     um determinado contexto, enquanto esconde detalhes menos importantes.*/

    //Herança:
    /* É criar novas classes baseadas em classes existentes, permitindo que as novas classes compartilhem atributos e
    comportamentos das classes originais, enquanto também podem adicionar características específicas ou modificar
    comportamentos conforme necessário.*/

    //Polimorfismo:
    /*É a capacidade de tratar diferentes objetos de maneira uniforme por meio de uma interface comum, permitindo que
     diferentes classes possam ser usadas de forma intercambiável, com cada classe executando seu próprio comportamento
     específico quando métodos comuns são chamados.*/

    //Encapsulamento:
    /*É o princípio de ocultar os detalhes internos de uma classe, protegendo seus atributos e comportamentos por meio
    da definição de níveis de acesso (como público, privado e protegido). Isso evita que partes externas do programa
    acessem diretamente os detalhes internos da classe, promovendo a modularidade, segurança e manutenção do código.*/

}
